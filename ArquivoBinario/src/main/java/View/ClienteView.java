    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        int id = Integer.parseInt(jTextFieldId.getText());
        String nome = jTextFieldNome.getText();
        String telefone = jTextFieldTelefone.getText();
        String endereco = jTextFieldEndereco.getText();

        ClienteModel cliente = new ClienteModel(id, nome, telefone, endereco);
        ControllerArquivoCliente controller = new ControllerArquivoCliente();
        if (controller.adicionarCliente(cliente)) {
            JOptionPane.showMessageDialog(null, "Cliente adicionado com sucesso!");
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao adicionar cliente.");
        }
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        int id = Integer.parseInt(jTextFieldId.getText());
        ControllerArquivoCliente controller = new ControllerArquivoCliente();
        ClienteModel cliente = controller.buscarCliente(id);
        if (cliente != null) {
            jTextFieldNome.setText(cliente.getNome());
            jTextFieldTelefone.setText(cliente.getTelefone());
            jTextFieldEndereco.setText(cliente.getEndereco());
            JOptionPane.showMessageDialog(null, "Os dados do cliente foram preenchidos.");

        } else {
            JOptionPane.showMessageDialog(null, "Cliente não encontrado.");
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

