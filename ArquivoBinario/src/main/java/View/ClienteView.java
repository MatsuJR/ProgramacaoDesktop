    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed
        int id = Integer.parseInt(jTextFieldId.getText());
        ControllerArquivoCliente controller = new ControllerArquivoCliente();
        if (controller.excluirCliente(id)) {
            JOptionPane.showMessageDialog(null, "Cliente excluído com sucesso!");
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao excluir cliente.");
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        int id = Integer.parseInt(jTextFieldId.getText());
        String nome = jTextFieldNome.getText();
        String telefone = jTextFieldTelefone.getText();
        String endereco = jTextFieldEndereco.getText();

        ClienteModel cliente = new ClienteModel(id, nome, telefone, endereco);
        ControllerArquivoCliente controller = new ControllerArquivoCliente();
        if (controller.atualizarCliente(id, cliente)) {
            JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso!");
            limparCampos();
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao atualizar cliente.");
        }

    }//GEN-LAST:event_jButtonAlterarActionPerformed

    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed
        ControllerArquivoCliente controller = new ControllerArquivoCliente();
    List<ClienteModel> clientes = controller.listarClientes();
    
    // Cria um JTextArea para exibir os clientes
    JTextArea textArea = new JTextArea(10, 30);
    textArea.setEditable(false); // Impede edição do texto
    
    // Adiciona os clientes ao JTextArea
    for (ClienteModel cliente : clientes) {
        textArea.append("ID: " + cliente.getId() + "\n");
        textArea.append("Nome: " + cliente.getNome() + "\n");
        textArea.append("Telefone: " + cliente.getTelefone() + "\n");
        textArea.append("Endereço: " + cliente.getEndereco() + "\n\n");
    }
    
    // Cria um JScrollPane para permitir a rolagem do JTextArea
    JScrollPane scrollPane = new JScrollPane(textArea);
    
    // Exibe o JOptionPane com o JScrollPane contendo o JTextArea
    JOptionPane.showMessageDialog(null, scrollPane, "Lista de Clientes", JOptionPane.PLAIN_MESSAGE);
    }//GEN-LAST:event_jButtonListarActionPerformed

