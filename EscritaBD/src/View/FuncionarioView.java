    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed
        try {
            if (jTextFieldIdF.getText().isEmpty()) {
                throw new IllegalArgumentException("O campo ID deve ser preenchido!");
            }

            int id = Integer.parseInt(jTextFieldIdF.getText());

            if (!funcionarioController.verificarIdExistente(id)) {
                JOptionPane.showMessageDialog(this, "Funcionário com ID " + id + " não encontrado");
                limparCampos();
                return;
            }

            FuncionarioModel funcionario = funcionarioController.consultarFuncionario(id);
            if (funcionario != null) {
                jTextFieldNomeF.setText(funcionario.getNomeFuncionario());
                jTextFieldCargoF.setText(funcionario.getCargo());
            } else {
                JOptionPane.showMessageDialog(this, "Funcionário não encontrado");
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O campo ID deve ser um número válido.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao consultar funcionário: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

