    private void jButtonListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonListarActionPerformed
        List<FuncionarioModel> funcionarios = funcionarioController.listarFuncionarios();
        if (funcionarios != null) {
            StringBuilder sb = new StringBuilder();
            for (FuncionarioModel funcionario : funcionarios) {
                sb.append("ID: ").append(funcionario.getIdFuncionario())
                        .append(", Nome: ").append(funcionario.getNomeFuncionario())
                        .append(", Cargo: ").append(funcionario.getCargo())
                        .append("\n");
            }
            JTextArea textArea = new JTextArea(10, 30);
            textArea.setText(sb.toString());
            textArea.setEditable(false);
            JScrollPane scrollPane = new JScrollPane(textArea);
            JOptionPane.showMessageDialog(null, scrollPane, "Funcionários", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Erro ao listar funcionários", "Erro", JOptionPane.ERROR_MESSAGE);
}    }//GEN-LAST:event_jButtonListarActionPerformed

