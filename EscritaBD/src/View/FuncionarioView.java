    private void jButtonAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAdicionarActionPerformed
        try {
            if (jTextFieldNomeF.getText().isEmpty() || jTextFieldCargoF.getText().isEmpty() || jTextFieldIdF.getText().isEmpty()) {
                throw new IllegalArgumentException("Os campos ID, Nome e Cargo devem ser preenchidos!");
            }

            int id = Integer.parseInt(jTextFieldIdF.getText());

            // Verifica se o ID já existe
            if (funcionarioController.verificarIdExistente(id)) {
                throw new IllegalArgumentException("Já existe um funcionário com este ID!");
            }

            String nome = jTextFieldNomeF.getText();
            String cargo = jTextFieldCargoF.getText();
            FuncionarioModel funcionario = new FuncionarioModel(id, nome, cargo);

            funcionarioController.inserirFuncionario(funcionario);
            limparCampos();
            JOptionPane.showMessageDialog(this, "Funcionário adicionado com sucesso!");

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O campo ID deve ser um número válido.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao adicionar funcionário: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAdicionarActionPerformed

    private void ExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirActionPerformed
        try {
            if (jTextFieldIdF.getText().isEmpty()) {
                throw new IllegalArgumentException("O campo ID deve ser preenchido!");
            }

            int id = Integer.parseInt(jTextFieldIdF.getText());
            if (!funcionarioController.verificarIdExistente(id)) {
                throw new IllegalArgumentException("Funcionário com ID " + id + " não encontrado.");
            }

            boolean sucesso = funcionarioController.excluirFuncionario(id);
            limparCampos();

            if (sucesso) {
                JOptionPane.showMessageDialog(this, "Funcionário excluído com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir funcionário.", "Erro", JOptionPane.ERROR_MESSAGE);
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O campo ID deve ser um número válido.", "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Erro de Validação", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao excluir funcionário: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ExcluirActionPerformed

